/**
 * Pull canonical theme tokens from Figma and emit three artifacts:
 *   - dist/tokens/tokens.css        (CSS custom properties, root-scoped)
 *   - dist/tokens/tokens.json       (raw token tree, consumed by stitch-sync-design-system)
 *   - dist/tokens/tailwind.preset.js (Tailwind preset object, consumed by tailwind.config.ts)
 *
 * Also writes a TS module under src/tokens/ so React components can import them
 * type-safely without a CSS variable indirection (handy for runtime style props).
 *
 * Implementer note: when running inside Claude Code, prefer the figma MCP
 * (`mcp__figma__get_variable_defs`) over the REST call below. From the CLI we
 * fall back to the Figma REST API.
 */
import path from "node:path";
import { writeFile } from "node:fs/promises";
import { ensureDir, writeJsonAtomic } from "./_lib/fs.js";
import { DIST_TOKENS, REPO_ROOT } from "./_lib/paths.js";

interface TokenTree {
  colors: Record<string, string>;
  typography: Record<string, { family: string; weight?: number; size?: number; lineHeight?: number }>;
  spacing: Record<string, number>;
  radii: Record<string, number>;
  shadows: Record<string, string>;
}

async function fetchFromFigma(): Promise<TokenTree> {
  const fileKey = process.env.FIGMA_FILE_KEY;
  const token = process.env.FIGMA_API_TOKEN;
  if (!fileKey || !token) {
    console.warn("update-tokens: FIGMA_FILE_KEY or FIGMA_API_TOKEN missing; emitting starter token tree.");
    return STARTER_TOKENS;
  }
  // Real Figma fetch would call REST: /v1/files/<key>/variables/local
  // The shape depends on the file's variable collections. Keep this stub so the
  // pipeline runs end-to-end; the implementer fills in the parsing once the
  // canonical Figma file's variable layout is known.
  const res = await fetch(`https://api.figma.com/v1/files/${fileKey}/variables/local`, {
    headers: { "X-Figma-Token": token },
  });
  if (!res.ok) {
    console.warn(`update-tokens: Figma fetch failed (${res.status}); falling back to starter tokens.`);
    return STARTER_TOKENS;
  }
  // Implementer: parse `await res.json()` into TokenTree. For now, fall back.
  await res.json();
  return STARTER_TOKENS;
}

