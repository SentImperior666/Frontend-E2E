function emitCss(t: TokenTree): string {
  const lines = [":root {"];
  for (const [n, v] of Object.entries(t.colors)) lines.push(`  --color-${n}: ${v};`);
  for (const [n, v] of Object.entries(t.spacing)) lines.push(`  --space-${n}: ${v}px;`);
  for (const [n, v] of Object.entries(t.radii)) lines.push(`  --radius-${n}: ${v}px;`);
  for (const [n, v] of Object.entries(t.shadows)) lines.push(`  --shadow-${n}: ${v};`);
  for (const [n, v] of Object.entries(t.typography)) {
    lines.push(`  --font-${n}: ${v.family};`);
    if (v.size) lines.push(`  --font-${n}-size: ${v.size}px;`);
    if (v.lineHeight) lines.push(`  --font-${n}-lh: ${v.lineHeight}px;`);
    if (v.weight) lines.push(`  --font-${n}-weight: ${v.weight};`);
  }
  lines.push("}");
  return lines.join("\n") + "\n";
}

function emitTailwindPreset(t: TokenTree): string {
  const colors = JSON.stringify(t.colors, null, 2);
  const spacing = JSON.stringify(
    Object.fromEntries(Object.entries(t.spacing).map(([k, v]) => [k, `${v}px`])),
    null,
    2,
  );
  const radii = JSON.stringify(
    Object.fromEntries(Object.entries(t.radii).map(([k, v]) => [k, `${v}px`])),
    null,
    2,
  );
  const shadows = JSON.stringify(t.shadows, null, 2);
  return `module.exports = {
  theme: {
    extend: {
      colors: ${colors},
      spacing: ${spacing},
      borderRadius: ${radii},
      boxShadow: ${shadows},
    },
  },
};
`;
}

function emitTs(t: TokenTree): string {
  return `// AUTO-GENERATED by scripts/update-tokens.ts. Do not edit.
export const tokens = ${JSON.stringify(t, null, 2)} as const;
export type Tokens = typeof tokens;
`;
}

async function main(): Promise<void> {
  const tokens = await fetchFromFigma();

  await ensureDir(DIST_TOKENS);
  await writeFile(path.join(DIST_TOKENS, "tokens.css"), emitCss(tokens), "utf8");
  await writeFile(path.join(DIST_TOKENS, "tailwind.preset.js"), emitTailwindPreset(tokens), "utf8");
  await writeJsonAtomic(path.join(DIST_TOKENS, "tokens.json"), tokens);

  const tsDir = path.join(REPO_ROOT, "src", "tokens");
  await ensureDir(tsDir);
  await writeFile(path.join(tsDir, "index.ts"), emitTs(tokens), "utf8");

  console.log(
    `update-tokens: emitted dist/tokens/{tokens.css,tokens.json,tailwind.preset.js} and src/tokens/index.ts (${Object.keys(tokens.colors).length} colors).`,
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
